-module(cl_idgen).

%% ------------------------------------------------------------------
%% API Function Exports
%% ------------------------------------------------------------------

-export([get_id/0]).

get_id() ->
	{M,S,U}=erlang:now(), 
	M*1000*1000*1000*1000+S*1000*1000+U.
